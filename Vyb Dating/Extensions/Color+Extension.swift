extension Color {
    static let primaryVybe = Color("ColorVybGreen")
    static let textFieldGrey = Color("ColorTextFieldGrey")
    static let vybGreen = Color("ColorDeepGreen")
    static let vybDeepGreen = Color("ColorVybDeepGreen")
    static let vybBackground = Color("ColorBackground")
}

extension UIColor {
    static let primaryVybe = UIColor.init(named: "ColorVybGreen")
    static let textFieldGrey = UIColor.init(named: "ColorTextFieldGrey")
    static let vybGreen = UIColor.init(named: "ColorDeepGreen")
    static let vybDeepGreen = UIColor.init(named: "ColorVybDeepGreen")
    static let vybBackground = UIColor.init(named: "ColorBackground")
}
